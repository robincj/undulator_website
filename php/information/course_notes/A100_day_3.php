<p>Start at Pinnacles car park (20 min down road from bach) and runs up
	the main river valley for a few minutes on left side of valley you will
	see a sign that says Pinnacles The High Way , take this route up to the
	main Aorangi Track. At the top you will turn left (right is towards
	Washpol where you have come from the day before!) then after a few
	minutes you will get to another intersection that says to Sunderlands
	Hut. Take this turn that descends to a river valley (stream) the 4 wd
	will cross the river and goes up other side. This is the start of a
	major undulation that will last for about 1- 1hr 30 min . Once you get
	to the tops it opens out and hopefully you will get some views. The
	only place you can get lost is the first major intersection that goes
	left or right. Take the right that descends this is the main ridge .
	the one that goes up and left is the wrong one and eventally drops to a
	major river valley DONT GO THAT WAY. After this its all undulating 4wd
	until you get to the big rocky descent to Sunderlands Hut valley. Once
	you get to bottom turn right and follow 4wd that keeps crossing river.
	It eventually gets to a last uphill.</p>
