<h2>A100 Spirit Award</h2>

<p>
	The <a href="/php/A100.php">Aorangi 100</a> is an unrelenting 3-day
	adventure trail running event that demands respect. Competitors must
	possess uncommon focus, drive and determination to complete the arduous
	course. A100 category winners deserve the praise heaped upon them, for
	they have excelled in an event that weeds out the weak.
</p>
<p>The A100 Spirit Award recognises that the event is more than
	competition and record setting. It is about adventure, and seeking that
	unique transcendence that only comes from breaking through your comfort
	zone. The A100 Spirit Award will be awarded each year to the competitor
	who, despite being uncomfortable, stressed and stretched, managed to
	remain positive, caring and helpful. Someone that clearly embodies the
	spirit of the Aorangi 100.</p>
<p>The A100 Spirit Award has been made in memory of trail running icon,
	Steven Neary. Steve not only won the inaugural A100, he truly embodied
	the spirit of the event with his infectious smile and lust for
	adventure.</p>
<p>He will be sadly missed.</p>
