<h2>Mandatory Equipment</h2>
<p>The following items of equipment must be carried by all Aorangi
	Undulator participants. The reasons for this should be obvious!
<p>All gear will be checked at the race start and competitors without
	these items will not be allowed to participate.
<p>A mobile phone is listed here, even though you may not be able to get
	phone reception at any point in the race, but it may work on the
	highest points, or if you have to make an emergency exit to a road end
	and need to call for help. Additionally, many modern mobile phones
	contain GPS receivers which may be useful for navigation in an
	emergency.
